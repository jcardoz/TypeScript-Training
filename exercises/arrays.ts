// Arrays can be defined in two ways
// First way is using the <Type>[] notation
let beatles: string[] = ['John', 'Paul', 'Ringo', 'George'];
console.log(beatles);

// The second is using the Array<Type> notation
let cars: Array<string> = ['Mercedes', 'Volvo', 'Saab'];
console.log(cars);

// Numeric Array
let primeNumbers: number[] = [2,3,5,7,11];
console.log(primeNumbers);

// Using Array<Type> notation
let oddNumbers: Array<number> = [1,3,5,7,9];
console.log(oddNumbers);